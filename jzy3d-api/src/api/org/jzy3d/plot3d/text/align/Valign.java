package org.jzy3d.plot3d.text.align;

import org.jzy3d.plot3d.text.renderers.TextBillboardRenderer;

/** 
 * Provides constants for defining the vertical alignment of a 
 * {@link TextBillboardRenderer}, w.r.t. its position.
 */
public enum Valign {
	/** Top vertical alignment.*/
	TOP,
	/** Ground vertical alignment.*/
	GROUND,
	/** Centered vertical alignment.*/
	CENTER,
	/** Bottom vertical alignment.*/
	BOTTOM
}
